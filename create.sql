CREATE TABLE user (

id INT NOT NULL AUTO_INCREMENT,
name VARCHAR(4000),
password VARCHAR(4000),
email VARCHAR(4000),
sex VARCHAR(4000),
country VARCHAR(4000),

PRIMARY KEY (id)
);


CREATE TABLE post (

id INT NOT NULL AUTO_INCREMENT,
topic VARCHAR(4000),
content VARCHAR(4000),
image MEDIUMBLOB,
time TIMESTAMP,

PRIMARY KEY (id)
); 



CREATE TABLE comment (

id INT NOT NULL AUTO_INCREMENT,
postid INT NOT NULL,
content VARCHAR(4000),

PRIMARY KEY (id)
)